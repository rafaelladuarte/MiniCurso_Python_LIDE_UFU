# Exemplos de Operadores de Atribuição em Economia

# Exemplo 1: Atribuição Simples
saldo_conta = 5000  # Saldo inicial da conta em dólares

# Exemplo 2: Atribuição com Adição
renda_mensal = 3000  # Renda mensal em dólares
gastos_mensais = 2000  # Gastos mensais em dólares

saldo_conta += renda_mensal  # Adicionando a renda à conta
saldo_conta -= gastos_mensais  # Subtraindo os gastos do saldo

print(f"Saldo após operações: {saldo_conta} dólares")

# Exemplo 3: Atribuição com Multiplicação
capital_inicial = 10000  # Capital inicial em dólares
taxa_juros = 0.05  # Taxa de juros (5%)

capital_final = capital_inicial * (1 + taxa_juros)  # Calculando o valor final com juros

print(f"Valor final do investimento: {capital_final} dólares")

# Exemplo 4: Atribuição com Concatenação de Strings
mensagem = "Preço: "
preco_produto = 50.99  # Preço do produto em dólares

mensagem += str(preco_produto)  # Concatenando o preço ao texto

print(mensagem)
