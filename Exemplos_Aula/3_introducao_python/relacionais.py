# Exemplos de Operadores Relacionais em Python

# Exemplo 1: Comparação de Receitas
receita_empresa1 = 50000  # Receita da primeira empresa em dólares
receita_empresa2 = 60000  # Receita da segunda empresa em dólares

comparacao_receita = receita_empresa1 < receita_empresa2
print(f"A primeira empresa tem uma receita menor que a segunda? {comparacao_receita}")

# Exemplo 2: Comparação de Gastos
gastos_mensais = 4000  # Gastos mensais de uma família em dólares
limite_gastos = 3500   # Limite de gastos permitidos em dólares

comparacao_gastos = gastos_mensais <= limite_gastos
print(f"Os gastos mensais estão dentro do limite permitido? {comparacao_gastos}")

# Exemplo 3: Comparação de Nomes
nome1 = "Empresa A"
nome2 = "Empresa B"

comparacao_nomes = nome1 != nome2
print(f"As empresas têm nomes diferentes? {comparacao_nomes}")

# Exemplo 4: Comparação de Taxas de Juros
taxa_juros_emprestimo = 0.06  # Taxa de juros de um empréstimo (6%)
taxa_juros_poupanca = 0.03   # Taxa de juros da poupança (3%)

comparacao_taxas = taxa_juros_emprestimo > taxa_juros_poupanca
print(f"A taxa de juros do empréstimo é maior do que a da poupança? {comparacao_taxas}")

# Exemplo 5: Comparação de Títulos de Investimento
titulo1 = "Título A"
titulo2 = "Título B"

comparacao_titulos = titulo1 < titulo2
print(f"O Título A está listado antes do Título B? {comparacao_titulos}")
